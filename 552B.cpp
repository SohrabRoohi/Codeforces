int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    
    int n;
    cin >> n;
    int64 answer = 0;
    while(n > 0) {
        double digits = ceil(log10(n));
        if(log10(n) == (int)log10(n)) {
            digits++;
        }
        int closestPower = (int)round(pow(10, digits - 1));
        int dist = n - closestPower + 1;
        answer += digits * dist;
        n -= dist;
    }
    cout << answer;
    return 0;
}